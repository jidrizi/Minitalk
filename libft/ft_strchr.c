#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*store;

	c = c % 256;
	store = 0;
	if (c == '\0')
	{
		while (*s)
			s++;
		return ((char *)s);
	}
	while (*s)
	{
		if (*s == c)
			return (store = (char *)s);
		s++;
	}
	return (0);
}
